    public static function lastNameMale()
    {
        return static::randomElement(static::$lastNameMale);
    }

    public static function lastNameFemale()
    {
        $lastName = static::randomElement(static::$lastNameMale);
        $lastNameEnding = mb_substr($lastName, -2);

        if (!isset(static::$femaleLastNameChangeable[$lastNameEnding])) {
            $lastNameLetter = mb_substr($lastNameEnding, -1);
            if (!in_array($lastNameLetter, static::$femaleLastNameExceptions)) {
                $lastName = $lastName.'а';
            }
        } else {
            $lastName = mb_substr($lastName, 0, -2);
            $lastName = $lastName.static::$femaleLastNameChangeable[$lastNameEnding];
        }

        return $lastName;
    }

    public function lastUserName($gender = null)
    {
        if ($gender === static::GENDER_MALE) {
            return static::lastNameMale();
        } elseif ($gender === static::GENDER_FEMALE) {
            return static::lastNameFemale();
        }

        return static::lastNameMale();
    }

    public function firstUserName($gender = null)
